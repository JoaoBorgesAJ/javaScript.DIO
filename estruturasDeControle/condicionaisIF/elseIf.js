/*Else If (senão se) : Às vezes, você tem várias condições para verificar. 
Para isso, você usa a instrução "else if" para verificar uma nova condição se a condição do "if" for falsa. 
Isso permite que você crie uma sequência de verificações condicionais.
*/
let nivelDeFome = 1
if (nivelDeFome === 1) {
    console.log("pouca fome")
} else if (nivelDeFome === 2) {
    console.log("muita fome")
} else {
    console.log("fome extrema")
}

